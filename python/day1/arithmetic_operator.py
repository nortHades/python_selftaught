print("=== division ===")

# 1. common division / return the float
result1 = 17 / 3
print(f"17 / 3 = {result1}")
print(f"type: {type(result1)}")

# 2. // return int
result2 = 17 // 3
print(f"17 // 3 = {result2}")
print(f"type: {type(result2)}")

# 3. %
result3 = 17 % 3
print(f"17 % 3 = {result3}")
print(f"type: {type(result3)}")

print("\n=== squard ===")

# **
print(f"5 ** 2 = {5 ** 2}")  # 25
print(f"2 ** 7 = {2 ** 7}")  # 2 to the power of 7 = 128
print(f"3 ** 4 = {3 ** 4}")  # 3 to the power of 4 = 81

# Special cases
print(f"Any number to power 0: 5 ** 0 = {5 ** 0}")  # 1
print(f"1 to any power: 1 ** 100 = {1 ** 100}")  # 1

# Fractional powers (roots)
print(f"Square root: 9 ** 0.5 = {9 ** 0.5}")  # 3.0
print(f"Cube root: 8 ** (1/3) = {8 ** (1/3)}")  # 2.0

# Negative powers
print(f"Reciprocal: 2 ** -1 = {2 ** -1}")  # 0.5
print(f"2 ** -3 = {2 ** -3}")  # 0.125

